#ifndef HAVE_SRANDOMDEV

#ifndef WIN32
#include <sys/time.h>
#include <unistd.h>
#endif

#include "os.h"
#include "log.h"
#include <fcntl.h>
#include <stdlib.h>
#include <time.h>

void
srandomdev(void)
{
  unsigned int seed;
  int seeded = 0;
#ifndef WIN32
  FILE *fd;
  fd = fopen("/dev/urandom", "r");
  if (NULL != fd) {
    if (fread(&seed, sizeof seed, 1, fd) != 1)
      WARNF("fread(\"/dev/urandom\") failed -- falling back to gettimeofday()");
    else
      seeded = 1;
    fclose(fd);
  }
#endif
  if (!seeded) {
    struct timeval tv;
    gettimeofday(&tv, NULL);
    seed = (getpid() << 16) ^ tv.tv_sec ^ tv.tv_usec;
  }
  srandom(seed);
}
#endif
